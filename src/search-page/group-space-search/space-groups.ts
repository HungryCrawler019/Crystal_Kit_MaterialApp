export const groups = keysForGrouping.reduce((acc, key) => {
  // we build a dictionnary of space groups per each propery
  const spaceGroupByKey = spaceGroups.reduce((innerAcc, spaceGroup) => {
    innerAcc[spaceGroup[key]]
      ? innerAcc[spaceGroup[key]].push(spaceGroup)
      : (innerAcc[spaceGroup[key]] = [spaceGroup]);
    return innerAcc;
  }, {});
  acc[key] = Object.entries(spaceGroupByKey).reduce((acc, [ikey, spaceGroupArray]) => {
    acc.push({
      label: groupKeyToDisplayKey[key]
        ? (spaceGroupArray as any)[0][groupKeyToDisplayKey[key]]
        : ikey,
      options: spaceGroupArray
    });
    return acc;
  }, [] as any[]);
  return acc;
}, {});

