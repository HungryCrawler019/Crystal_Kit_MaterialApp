function PeriodicElement({ element, enabled = false, disabled = false, onElementClicked }) {
    const handleClick = (element) => {
        onElementClicked(element);
    };
    return (React.createElement("div", { onClick: () => handleClick(element), className: `mat-element  ${element.class} ${(enabled && !disabled) ? 'enabled' : ''} ${disabled ? 'disabled' : ''} ` },
        React.createElement("div", { className: "number" }, element.elementNumber),
        React.createElement("div", { className: "symbol" }, element.symbol)));
}

