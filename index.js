function TableSpacer({ onTableSwitcherClicked }) {
    return (React.createElement(React.Fragment, null,
        React.createElement("div", { className: "first-span" },
            React.createElement("div", { className: "table-switcher", onClick: onTableSwitcherClicked }),
            React.createElement("div", { className: "input-container" }, "Some dynamic input there based on the app")),
        React.createElement("div", { className: "second-span" }),
        React.createElement("div", { className: "third-span" }),
        React.createElement("div", { className: "separator-span" }),
        React.createElement("div", { className: "first-lower-span" }),
        React.createElement("div", { className: "second-lower-span" })));
}
function Table({ disabledElement, enabledElement, onElementClicked }) {
    const [isShown, setIsShown] = React.useState(true);
    return (React.createElement("div", { className: `table-container ${isShown ? '' : 'elements-hidden'}` },
        React.createElement(TableSpacer, { onTableSwitcherClicked: () => setIsShown(!isShown) }),
        TABLE.map((element) => React.createElement(PeriodicElement, { onElementClicked: (element) => onElementClicked(element), key: `${element.symbol}--${element.elementNumber}`, disabled: disabledElement[element.symbol], enabled: enabledElement[element.symbol], element: element }))));
}

